/// Zoom session state enumeration.
/// حالة جلسة Zoom.
///
/// Represents the current state of a Zoom video session.
///
/// **States:**
/// - [idle]: No active connection / لا يوجد اتصال نشط
/// - [connecting]: Attempting to join session / جاري الاتصال
/// - [connected]: Successfully joined and in active session / متصل بجلسة نشطة
/// - [error]: Connection or session error occurred / حدث خطأ
///
/// **Usage:**
/// ```dart
/// zoomService.sessionStateStream.listen((state) {
///   switch (state) {
///     case ZoomSessionState.idle:
///       print('Not in session');
///       break;
///     case ZoomSessionState.connecting:
///       showLoadingIndicator();
///       break;
///     case ZoomSessionState.connected:
///       showVideoUI();
///       break;
///     case ZoomSessionState.error:
///       showErrorMessage();
///       break;
///   }
/// });
/// ```
enum ZoomSessionState {
  /// لا يوجد اتصال نشط
  idle,

  /// جاري الاتصال
  connecting,

  /// متصل بجلسة نشطة
  connected,

  /// حدث خطأ
  error,
}

