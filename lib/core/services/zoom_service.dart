/// - Broadcast session state changes via streams / بث تغييرات حالة الجلسة عبر streams
///
/// **Security / ملاحظات الأمان:**
/// - No secret keys stored in the app / لا يتم تخزين أي مفاتيح سرية في التطبيق
/// - All JWT tokens generated server-side / جميع JWT Tokens يتم إنشاؤها من جانب الخادم
