///
/// **Session State Management:**
/// The service broadcasts session state changes via [sessionStateStream]:
/// - `idle`: No active connection
/// - `connecting`: Attempting to join session
/// - `connected`: Successfully joined session
/// - `error`: Connection or session error
///
/// **Placeholder Mode:**
/// Currently, all methods log their intent but don't execute actual SDK calls.
/// This allows the app to compile and run while waiting for Zoom SDK credentials.
